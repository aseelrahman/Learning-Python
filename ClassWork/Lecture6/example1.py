# Accessing Values

# Create a dictionary with two key/value pairs.
my_dictionary = {'color': 'green', 'points': 5}

# Assign the value 5 to points variable.
points = my_dictionary["points"]

# Format the output using the F-String
print(f"You've just earned {points} points!")
print(f"Do you like the color {my_dictionary['color']}?")